package main

import "fmt"

func main() {
	// concadenaciones de strings
	fmt.Println("go" + "lang")

	// concadenaciones de sttings e integers
	fmt.Println("1+1 =", 1+1)
	fmt.Println("7.0/3.0 =", 7.0/3.0)

	// imprecion de booleanos
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!false)
}
